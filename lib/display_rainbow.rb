def display_rainbow (colors = ['red','orange','yellow','green','blue','indigo','violet'])
  puts "R:{#{colors[0]}}"
  puts "O:{#{colors[1]}}"
  puts "Y:{#{colors[2]}}"
  puts "G:{#{colors[0]}}"
  puts "B:{#{colors[0]}}"
  puts "I:{#{colors[0]}}"
  puts "V:{#{colors[0]}}"
end
